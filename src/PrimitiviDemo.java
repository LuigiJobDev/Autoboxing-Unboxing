public class PrimitiviDemo {
    public static void mostraEsempio(){
        int numero = 10; /*Tipo primitivo*/
        double decimale = 5.5;
        char carattere = 'L';
        boolean valoreLogico = true;

        System.out.println("Numero (int): " + numero);
        System.out.println("Decimale (double): " + decimale);
        System.out.println("Carattere (char): " + carattere);
        System.out.println("Valore logico (boolean): " + valoreLogico);
    }
}
